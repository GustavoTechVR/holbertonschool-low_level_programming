#include "3-calc.h"

/**
 * op_add - int
 * op_sub - int
 * op_mul - int
 * op_div - int
 * op_mod - int
 * @a: the character checked
 * @b: the character checked
 * Description: program that performs simple operations
 * Return: 0
 */

int op_add(int a, int b)
{
	return (a + b);
}

int op_sub(int a, int b)
{
	return (a - b);
}

int op_mul(int a, int b)
{
	return (a * b);
}

int op_div(int a, int b)
{
	return (a / b);
}

int op_mod(int a, int b)
{
	return (a % b);
}
